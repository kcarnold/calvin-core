/**
 * One-shot script: parse catalog.html → catalog-categories.json
 *
 * Extracts the pure-data fields from parseCoreProgram() output, stripping
 * all DOM element references. The resulting JSON is hand-verifiable,
 * diffable, and loads instantly (no JSDOM needed for logic tests).
 *
 * Usage:
 *   node test/generate-catalog-json.js
 *
 * Re-run when the catalog changes (typically once per academic year).
 */

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { JSDOM } from 'jsdom';
import { parseCoreProgram } from '../src/core.js';

const __dirname = fileURLToPath(new URL('.', import.meta.url));
const CATALOG_HTML = join(__dirname, 'fixtures', 'catalog.html');
const OUTPUT = join(__dirname, 'fixtures', 'catalog-categories.json');

if (!existsSync(CATALOG_HTML)) {
  console.error(`Missing ${CATALOG_HTML}. Run: npm test  (it auto-fetches the catalog).`);
  process.exit(1);
}

const html = readFileSync(CATALOG_HTML, 'utf-8');
const doc = new JSDOM(html).window.document;
const raw = parseCoreProgram(doc);

// Strip DOM-only fields, keep everything analyze() needs
const categories = raw.map(cat => {
  const base = {
    type: cat.type,
    section: cat.section,
    name: cat.name,
    instructions: cat.instructions,
    coreIndex: cat.coreIndex,
    slug: cat.slug,
  };

  if (cat.type === 'section-header') return base;

  return {
    ...base,
    reqType: cat.reqType,
    minHours: cat.minHours,
    maxHours: cat.maxHours,
    maxPerDiscipline: cat.maxPerDiscipline,
    courses: (cat.courses || []).map(c => ({
      code: c.code,
      title: c.title,
      subDiscipline: c.subDiscipline,
    })),
  };
});

const output = {
  _meta: {
    catalogYear: '2025-26',
    generatedFrom: 'catalog.html',
    generatedAt: new Date().toISOString().slice(0, 10),
    note: 'Auto-generated by test/generate-catalog-json.js. Re-run when catalog changes.',
  },
  categories,
};

writeFileSync(OUTPUT, JSON.stringify(output, null, 2) + '\n', 'utf-8');

// Print summary
const sections = categories.filter(c => c.type === 'section-header');
const cats = categories.filter(c => c.type !== 'section-header');
const totalCourses = cats.reduce((n, c) => n + (c.courses?.length || 0), 0);
console.log(`Wrote ${OUTPUT}`);
console.log(`  ${sections.length} sections, ${cats.length} categories, ${totalCourses} total courses`);
for (const cat of cats) {
  const courses = cat.courses?.length || 0;
  const details = [cat.reqType];
  if (cat.minHours != null) details.push(`${cat.minHours}–${cat.maxHours}h`);
  if (cat.maxPerDiscipline) details.push(`max ${cat.maxPerDiscipline}h/disc`);
  console.log(`  ${cat.name}: ${courses} courses (${details.join(', ')})`);
}
